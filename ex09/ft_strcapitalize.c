char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] += 32;
		i++;
	}
	return (str);
}

char	*ft_strcapitalize(char *str)
{
	int	i;
	int	first_letter;

	i = 0;
	first_letter = 1;
	ft_strlowcase(str);
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			if (first_letter == 1)
				str[i] -= 32;
				first_letter = 0;
		}
		else if (str[i] >= '0' && str[i] <= '9')
			first_letter = 0;
		else
			first_letter = 1;
		i++;
	}
	return (str);
}

